#include "CppCode.hpp"
#include <iostream>

using namespace std;

float CppClass::addition(float num1, float num2) {
    return num1 + num2;
}

float CppClass::subtraction(float num1, float num2) {
    return num1 - num2;
}

float CppClass::multiplication(float num1, float num2) {
    return num1 * num2;
}

float CppClass::division(float num1, float num2) {
    return num1 / num2;
}
